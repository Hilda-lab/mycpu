#!/bin/bash

# myCPU 快速测试脚本

echo "=========================================="
echo "  myCPU 快速测试脚本"
echo "=========================================="
echo ""

# 检查是否已编译
if [ ! -f "./build/mycpu" ]; then
    echo "📦 编译项目..."
    cmake -S . -B build
    cmake --build build -j
    echo ""
fi

echo "=========================================="
echo "【测试 1】自动化测试"
echo "=========================================="
ctest --test-dir build --output-on-failure
echo ""

echo "=========================================="
echo "【测试 2】主程序测试"
echo "=========================================="
./build/mycpu
echo ""

echo "=========================================="
echo "【测试 3】简单功能测试"
echo "=========================================="
./build/test_simple
echo ""

echo "=========================================="
echo "✅ 所有测试完成！"
echo "=========================================="
